import 'package:flutter/material.dart';

SizedBox addVerticalSpace(double height) {
  return SizedBox(
    height: height,
  );
}

SizedBox addHorizontalSpace(double width) {
  return SizedBox(
    width: width,
  );
}

SizedBox addVertical(double height) {
  return SizedBox(
    height: height,
  );
}

SizedBox addHorizontal(double width) {
  return SizedBox(
    width: width,
  );
}
